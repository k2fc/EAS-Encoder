    public class EASMessage
    {
        private static readonly string Preamble = "\xab\xab\xab\xab\xab\xab\xab\xab\xab\xab\xab\xab\xab\xab\xab\xab";

        private readonly string _code;
        private readonly string _length;
        private readonly string _originator;
        private readonly List<SAMERegion> _regions;
        private string _sender;
        private DateTime _start;
        private SAMEMessageOriginator _messageOriginator;
        private SAMEMessageAlertCode _messageAlertCode;

        public string Code { get { return _code; } }
        public TimeSpan Length
        {
            get
            {
                return new TimeSpan(Convert.ToInt32(_length.Substring(0, 2)), Convert.ToInt32(_length.Substring(2, 2)), 0);
            }
        }

        public string Originator { get { return _originator; } }
        public List<SAMERegion> Regions { get { return _regions; } }
        public string Sender
        {
            get { return _sender; }
            set { _sender = value; }
        }
        public DateTime Start { get { return _start; } }
        public DateTime End { get { return this.Start.Add(this.Length); } }
        public EASMessage(string Originator, string Code, List<SAMERegion> Regions, string Length,
            DateTime Start,
            string Sender)
        {
            _originator = Originator;
            _code = Code;
            _regions = Regions;
            _length = Length;
            _start = Start;
            _sender = Sender;
            _messageAlertCode = MessageTypes.AlertCodes.Find(x => x.Id == _code);
            _messageOriginator = MessageTypes.Originators.Find(x => x.Id == _originator);
        }

        public bool Equals(EASMessage obj)
        {
            if (obj == null)
            {
                return false;
            }
            return _code == obj.Code && this.Length == obj.Length && _originator == obj.Originator &&
                _regions == obj.Regions && _start == obj.Start;
        }

        internal string ZeroPad(string String, int Length)
        {
            if (string.IsNullOrEmpty(String))
            {
                String = "0";
            }
            if (String.Length > Length)
            {
                return String;
            }

            while (String.Length < Length)
            {
                String = "0" + String;
            }

            return String;
        }

        internal string ToSameEndOfMessageString()
        {
            return $"{Preamble}{"NNNN"}";
        }

        internal string ToSameHeaderString()
        {
            var headerRegions = _regions.Aggregate(string.Empty,
                (current, thisRegion) =>
                    current +
                    $"{ZeroPad(thisRegion.Subdivision.Id.ToString(), 1)}{ZeroPad(thisRegion.State.Id.ToString(), 2)}{ZeroPad(thisRegion.County.Id.ToString(), 3)}-");
            headerRegions = headerRegions.Remove(headerRegions.Length - 1);

            return $"{Preamble}" +
                   $"{"ZCZC"}-" +
                   $"{_originator}-" +
                   $"{_code}-" +
                   $"{headerRegions}+" +
                   $"{_length}-" +
                   $"{ZeroPad(_start.DayOfYear.ToString(), 3)}" +
                   $"{ZeroPad(_start.Hour.ToString(), 2)}" +
                   $"{ZeroPad(_start.Minute.ToString(), 2)}-" +
                   $"{_sender}-";
        }

        public override string ToString()
        {
            string friendlyString = _messageOriginator.Name + " has issued a " + _messageAlertCode.Name + " for ";
            for (int i = 0; i < Regions.Count; i++)
            {
                friendlyString += Regions[i].CountyName + ", ";
                if (Regions.Count > 1 && i == Regions.Count - 2)
                    friendlyString += "and ";
            }
            friendlyString += "beginning " + _start.ToLocalTime().ToShortDateString() +" " + _start.ToLocalTime().ToShortTimeString() + 
                " and ending " + End.ToLocalTime().ToShortDateString() + " " + End.ToLocalTime().ToShortTimeString() + ". (" + _sender + ")";
            return friendlyString;
        }
    }
